void find_second_max(float32_t *array, uint32_t length, float32_t *second_max,uint32_t *second_max_index){
		float32_t max_val;
    uint32_t max_index;
    float32_t temp_max = 0;  // 设定一个极小的初始值
    uint32_t temp_max_index = 0;
    // Step 1: 找到最大值
    arm_max_f32(array, length, &max_val, &max_index); 
    // Step 2: 遍历数组找到次大值及其索引
    for (uint32_t i = 0; i < length; i++) {
        if (((i<max_index-offset)||(i>max_index+offset)) && array[i] > temp_max&&(i>15)) {//offset 表示加窗，滤除频谱泄露
            temp_max = array[i];
            temp_max_index = i;
        }
	}
    *second_max = temp_max;
    *second_max_index = temp_max_index;
}

